    public DependencyProblemPanel(String fromGradle, String fromTemplate) {
        initComponents();
        String[] splitGradle = fromGradle.split(":");
        String[] splitTemplate = fromTemplate.split(":");
        if (splitGradle.length == 3) {
            gradleGroupId.setText(splitGradle[0]);
            gradleArtifactId.setText(splitGradle[1]);
            gradleVersion.setText(splitGradle[2]);
        }
        if (splitTemplate.length == 3) {
            templateGroupId.setText(splitTemplate[0]);
            templateArtifactId.setText(splitTemplate[1]);
            templateVersion.setText(splitTemplate[2]);
        }
    }

    public boolean useFromTemplate() {
        return template.isSelected();
    }

