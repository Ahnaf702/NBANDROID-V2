    private void cbxStatsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cbxStatsActionPerformed
        controller.setEnableStats(cbxStats.isSelected());
    }//GEN-LAST:event_cbxStatsActionPerformed

  @Override
  public void load() {
    if (docListener != null) {
      jTextFieldSdkLocation.getDocument().removeDocumentListener(docListener);
    }
    jTextFieldSdkLocation.setText(DalvikPlatformManager.getDefault().getSdkLocation());
    jTextFieldSdkLocation.getDocument().addDocumentListener(docListener);
    cbxStats.setSelected(controller.isEnableStats());
  }

  @Override
  public void store() {
    DalvikPlatformManager.getDefault().setSdkLocation(jTextFieldSdkLocation.getText());
  }

  @Override
  public boolean valid() {
    // TODO(radim): move this to a better place?
    File sdkLocation = new File(jTextFieldSdkLocation.getText());
    File sdkTools = new File(sdkLocation, "tools");
    return sdkLocation.exists() && sdkLocation.isDirectory() &&
        sdkTools.exists() && sdkTools.isDirectory();
  }

