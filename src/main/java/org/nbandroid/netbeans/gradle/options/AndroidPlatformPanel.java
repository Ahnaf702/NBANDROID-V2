  private void jButtonSdkLocatioBrowseActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSdkLocatioBrowseActionPerformed
    File sdkDir = new FileChooserBuilder(AndroidPlatformPanel.class)
        .setDirectoriesOnly(true)
        .setTitle("Choose Android SDK directory")
        .showOpenDialog();
    if (sdkDir != null) {
      jTextFieldSdkLocation.setText(sdkDir.getAbsolutePath());
    }
  }//GEN-LAST:event_jButtonSdkLocatioBrowseActionPerformed

  private void jLabel2MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabel2MouseClicked
    try {
      URLDisplayer.getDefault().showURL(new URL("http://developer.android.com/sdk/index.html"));
    } catch (MalformedURLException ex) {
      Exceptions.printStackTrace(ex);
    }
  }//GEN-LAST:event_jLabel2MouseClicked

